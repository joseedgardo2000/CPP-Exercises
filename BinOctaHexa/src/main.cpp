#include <iostream>

int main()
{
	int a = 10;
	int b = 010;
	int c = 0x10;
	int d = 0b10;

	std::cout << "decimal (10): " << a << std::endl;
	std::cout << "octal (10): " << b << std::endl;
	std::cout << "hexadecimal (10): " << c << std::endl;
	std::cout << "binario (10): " << d << std::endl;
}


