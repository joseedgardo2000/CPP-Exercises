#include <iostream>

int main()
{
	int a=0, b=0, c=0, d=0;

	std::cout <<"a: " << a << " b: " << b << std::endl;
	std::cout <<"a++: " << a++ << " ++b: " << ++b << std::endl;
	std::cout <<"a: " << a << " b: " << b << std::endl;

	std::cout <<"c: " << c << " d: " << d << std::endl;
	std::cout <<"c--: " << c-- << " --d: " << --d << std::endl;
	std::cout <<"c: " << c << " d: " << d << std::endl;

	return 0;
}



